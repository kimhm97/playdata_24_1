
# 일반데이터 출력
print("정수: 양의정수", 10)
print("정수: 음의정수", -10)
print("정수: 0", 0)
print("정수: 10진법(10표현)", 10)
print("정수: 2진법", 0b10) # 2
print("정수: 8진법", 0o10)  # 8
print("정수: 16진법", 0x10) # 16
print("실수: ", 3.14)
print("실수(지수표기법: ", 3e+5) # 300000.0

print("논리: ", True, False) # True/False가 아니어도 논리값으로 사용이 가능하다.

